public class Delapan {
	public static void main(String[] args) {
		//inisialisasi awal
		int i=1;
		//Perulangan untuk menentukan batas angka akhir
		while (i<=110){
			//perulangan untuk membuat keluaran menjadi per baris 11 angka saja
			for(int j=1;j<=11;j++) {
				System.out.print(i+" ");
				i++;
				}
				//mengganti baris
				System.out.println();
				
		}
				
				
		
	}
	
}